def write_outputs(project_root: Path, contents: dict[str, str], allow_overwrite: bool) -> list[Path]:
    targets = [project_root / rel for rel in contents]
    existing = [path for path in targets if path.exists()]
    if existing and not allow_overwrite:
        raise FileExistsError("refusing to overwrite existing files: " + ", ".join(path.relative_to(project_root).as_posix() for path in existing))
    written: list[Path] = []
    for rel, text in contents.items():
        path = project_root / rel
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(text, encoding="utf-8")
        written.append(path)
    return written


def parse_args(argv: list[str]) -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--project-root", required=True, help="Song project root.")
    parser.add_argument("--write", action="store_true", help="Write catalog memory files.")
    parser.add_argument("--allow-overwrite", action="store_true", help="Allow overwriting catalog memory files.")
    parser.add_argument("--strict", action="store_true", help="Return nonzero when core evidence is missing.")
    return parser.parse_args(argv)


def main(argv: list[str]) -> int:
    args = parse_args(argv)
    project_root = Path(args.project_root).expanduser().resolve()
    if not project_root.exists():
        print(f"error: project root does not exist: {project_root}", file=sys.stderr)
        return 2
    evidence = load_evidence(project_root)
    if evidence.missing:
        print("catalog memory writeback blocked:", file=sys.stderr)
        for rel in evidence.missing:
            print(f"- missing {rel}", file=sys.stderr)
        if args.strict:
            return 2

    contents = output_contents(evidence)
    if args.write:
        try:
            written = write_outputs(project_root, contents, args.allow_overwrite)
        except FileExistsError as exc:
            print(f"error: {exc}", file=sys.stderr)
            return 2
        for path in written:
            print(f"wrote {path.relative_to(project_root).as_posix()}")
    else:
        print("catalog memory writeback preview")
        print(f"song: {evidence.song}")
        print(f"release decision: {evidence.release_decision}")
        print(f"catalog promotion: {evidence.catalog_promotion}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main(sys.argv[1:]))
