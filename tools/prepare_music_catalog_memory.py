def route_lines(evidence: ProjectEvidence) -> str:
    if not evidence.route_rows:
        return "- no repair route rows found"
    lines: list[str] = []
    for row in evidence.route_rows[:8]:
        problem = row.get("problem", "").strip()
        layer = row.get("layer", "").strip()
        route = (row.get("repair_route") or row.get("route") or "").strip()
        severity = row.get("severity", "").strip()
        lines.append(f"- {problem} | layer: {layer} | severity: {severity} | route: {route}")
    return "\n".join(lines)


def promotion_note(evidence: ProjectEvidence) -> str:
    if evidence.promotion_ready:
        return "Promote only the rules that are supported by review, repair, and release-candidate evidence."
    return "Do not promote this song as a catalog standard yet; capture failure lessons and banned defaults."


