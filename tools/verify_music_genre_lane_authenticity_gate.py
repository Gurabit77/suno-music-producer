def exclude_text() -> str:
    return """# Exclude Negative Aesthetic

Exclude: random genre switch, generic AI pop gloss, EDM drop, fake crowd, overproduced karaoke sheen, protected artist names
"""


def main() -> int:
    errors: list[str] = []
    with tempfile.TemporaryDirectory(prefix="ai-music-genre-lane-") as tmp:
        project = scaffold(Path(tmp))

        missing_specificity = run([sys.executable, str(AUDIT), "--project-root", str(project), "--strict"])
        if missing_specificity.returncode == 0:
            errors.append("genre/lane audit should fail when prompt specificity is missing")
        if "prompt_specificity_not_ready" not in (missing_specificity.stdout + missing_specificity.stderr):
            errors.append("missing-specificity failure should name prompt_specificity_not_ready")

        write(project / "04_prompt" / "prompt-specificity-budget-audit.md", specificity_pass(project))
        write(project / "04_prompt" / "style-field-map.md", bad_style())
        write(project / "04_prompt" / "lyrics-context-map.md", lyrics_context())
        write(project / "04_prompt" / "exclude-negative-aesthetic.md", exclude_text())

        bad = run([sys.executable, str(AUDIT), "--project-root", str(project), "--write", "--allow-overwrite", "--strict"])
        if bad.returncode == 0:
            errors.append("genre soup without rationale should fail")
        for term in ["genre_soup_without_rationale", "scene_anchor_missing"]:
            if term not in (bad.stdout + bad.stderr):
                errors.append(f"bad genre/lane audit missing blocker: {term}")

        compile_without_audit = run([sys.executable, str(COMPILER), "--project-root", str(project), "--strict"])
        if "genre_lane_authenticity_audit" not in (compile_without_audit.stdout + compile_without_audit.stderr):
            errors.append("compiler should surface genre_lane_authenticity_audit when audit is absent or failed")

        write(project / "04_prompt" / "style-field-map.md", good_style())
        ok = run([sys.executable, str(AUDIT), "--project-root", str(project), "--write", "--allow-overwrite", "--strict"])
        if ok.returncode != 0:
            errors.append(f"good genre/lane audit should pass: stdout={ok.stdout.strip()} stderr={ok.stderr.strip()}")
        audit_text = (project / "04_prompt" / "genre-lane-authenticity-audit.md").read_text(encoding="utf-8")
        for term in [
            "Generated by: tools/audit_music_genre_lane_authenticity.py",
            "Decision: pass",
            "Primary lane: Mandarin night pop-rock",
            "Genre/lane authenticity: pass",
            "Prompt compile handoff: pass",
        ]:
            if term not in audit_text:
                errors.append(f"genre/lane audit output missing term: {term}")

        no_overwrite = run([sys.executable, str(AUDIT), "--project-root", str(project), "--write"])
        if no_overwrite.returncode != 2:
            errors.append("genre/lane audit should refuse overwrite without --allow-overwrite")

        write(project / "04_prompt" / "style-field-map.md", good_style().replace("Mandarin night pop-rock", "Taylor Swift style Mandarin pop-rock"))
        protected = run([sys.executable, str(AUDIT), "--project-root", str(project), "--write", "--allow-overwrite", "--strict"])
        if protected.returncode == 0:
            errors.append("protected identity in genre lane should fail")
        if "protected_identity_in_lane" not in (protected.stdout + protected.stderr):
            errors.append("protected-identity failure should name protected_identity_in_lane")

    compiler_source = COMPILER.read_text(encoding="utf-8")
    for term in ["genre_lane_authenticity_audit", "Genre/lane authenticity", "tools/audit_music_genre_lane_authenticity.py"]:
        if term not in compiler_source:
            errors.append(f"compiler missing genre/lane integration term: {term}")

    for path, label in [(WIKI_PROMPT, "prompt"), (WIKI_PROJECT, "project"), (WIKI_STATUS, "status")]:
        text = path.read_text(encoding="utf-8") if path.exists() else ""
        for term in ["tools/audit_music_genre_lane_authenticity.py", "genre-lane-authenticity-audit.md", "Genre/lane authenticity"]:
            if term not in text:
                errors.append(f"{label} wiki page missing genre/lane term: {term}")
    log_text = LOG.read_text(encoding="utf-8") if LOG.exists() else ""
    if "AI 音乐 Genre Lane Authenticity 工具闸门" not in log_text:
        errors.append("wiki log missing genre/lane authenticity gate entry")

    if errors:
        print("AI music genre/lane authenticity gate verification failed:", file=sys.stderr)
        for error in errors:
            print(f"- {error}", file=sys.stderr)
        return 1

    print("AI music genre/lane authenticity gate verification passed.")
    print(f"tool: {AUDIT}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
