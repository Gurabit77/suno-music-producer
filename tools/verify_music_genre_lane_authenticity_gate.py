#!/usr/bin/env python3
"""Verify the AI music genre/lane authenticity gate."""

from __future__ import annotations

import subprocess
import sys
import tempfile
from pathlib import Path


ROOT = Path("/path/to/suno-music-producer")
SCAFFOLD = ROOT / "tools" / "create_music_song_project.py"
AUDIT = ROOT / "tools" / "audit_music_genre_lane_authenticity.py"
COMPILER = ROOT / "tools" / "compile_music_prompt_package.py"
WIKI_PROMPT = Path("/path/to/obsidian-vault/wiki/分析/音乐/AI 音乐 Prompt 编译与生成前预检工作流.md")
WIKI_PROJECT = Path("/path/to/obsidian-vault/wiki/分析/音乐/AI 音乐单曲项目文件夹与证据包模板.md")
WIKI_STATUS = Path("/path/to/obsidian-vault/wiki/分析/音乐/AI 音乐单曲制作总控与阶段闸门工作流.md")
LOG = Path("/path/to/obsidian-vault/wiki/log.md")


def run(cmd: list[str], cwd: Path = ROOT) -> subprocess.CompletedProcess[str]:
    return subprocess.run(cmd, cwd=cwd, text=True, capture_output=True)


def write(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8")


def scaffold(tmp_path: Path) -> Path:
    cmd = [
        sys.executable,
        str(SCAFFOLD),
        "--output-dir",
        str(tmp_path),
        "--date",
        "20260625",
        "--language",
        "zh",
        "--lane",
        "pop",
        "--slug",
        "genre-lane",
        "--title",
        "Night Bus Confession",
        "--artist-project",
        "Example Test Artist",
        "--use-case",
        "demo",
    ]
    result = run(cmd)
    if result.returncode != 0:
        raise RuntimeError(result.stderr)
    return tmp_path / "20260625_zh-pop_genre-lane"


def specificity_pass(project: Path) -> str:
    return f"""# Prompt Specificity Budget Audit

Generated by: tools/audit_music_prompt_specificity_budget.py
Project root: {project}
Decision: pass

## Specificity Budget Evidence

Style source: 04_prompt/style-field-map.md
Budget source: 04_prompt/field-length-and-specificity-budget.md
Methodology transfer: pass
Style specificity tags: Mandarin night pop-rock, close mid-low female lead, live drums, melodic bass, clean electric guitar, dry verse vocal, wide but sparse chorus
Recording realism tags: dry, close, room, wide, tape
Performance realism tags: lead vocal, drums, bass, guitar, pocket
Section/director tags: verse, chorus, title phrase, hook
Field budget tags: must-hear=dry verse vocal, melodic bass, title phrase hook, decision=ready
Vague term removals: emotional, catchy, realistic
Protected identity check: pass
Prompt compile handoff: pass

## Findings

| severity | code | message | route |
|---|---|---|---|
| pass | none | specificity budget approved | prompt compile |
"""


