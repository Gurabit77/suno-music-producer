def build_prompt_package(brief: str, route: RoutePlan, reference_dna: ReferenceDNA) -> PromptPackage:
    language = detect_language(brief)
    title = infer_title_phrase(brief)
    lyrics = build_lyrics(title, brief, language)
    style = (
        f"genre: {infer_genre(brief)}; "
        f"vocal: {infer_vocal(brief)}; "
        f"instrumentation: {infer_instrumentation(brief)}; "
        f"production: {infer_production(brief)}; "
        f"mood: {infer_mood(brief)}"
    )
    if route.decision in {"ordinary_generation_with_reference_dna", "blocked_reference_upload_use_listen_only_dna"}:
        style += f"; reference DNA: {reference_dna.tempo}, {reference_dna.harmonic_color}, {reference_dna.energy_curve}"
    if route.decision.startswith("reference_upload"):
        style += "; uploaded seed target: retain authorized harmonic grounding while creating new topline and arrangement"
    exclude = (
        "robotic vocal, muddy mix, harsh sibilance, abrupt ending, random rap verse, "
        "artist imitation, copied melody, copied lyrics, recognizable riff, reference singer voice, overcompressed chorus"
    )
    changed_variable = "initial_prompt"
    if route.decision.startswith("reference_upload"):
        changed_variable = "audio_seed_or_chord_skeleton"
    elif "人声" in brief or "vocal" in brief.lower():
        changed_variable = "vocal_identity"
    elif "副歌" in brief or "chorus" in brief.lower():
        changed_variable = "structure_energy"
    return PromptPackage(
        title=title,
        creative_brief=f"{infer_use_case(brief)} / {route.decision}: {normalize(brief)}",
        lyrics=lyrics,
        style=style,
        exclude=exclude,
        model_settings="Model: current best visible Suno model; Custom Mode: on; Batch: 2; Weirdness: 30-45; Style Influence: 60-80; Audio Influence: 0 unless approved self-owned/authorized seed is active",
        batch_plan="Generate 2 candidates with one primary variable held accountable; do not broaden rewrite after approval.",
        review_rubric="first_20s, hook_memory, vocal_identity, lyrics_intelligibility, groove, chorus_lift, arrangement, ai_artifacts, fit_to_brief, similarity_risk, user_preference, next_action",
        changed_variable=changed_variable,
    )


def style_segments(style: str) -> dict[str, str]:
    segments: dict[str, str] = {}
    for part in style.split(";"):
        if ":" not in part:
            continue
        key, value = part.split(":", 1)
        segments[key.strip().casefold()] = value.strip()
    return segments


def lint_prompt(prompt: PromptPackage, route: RoutePlan) -> list[LintIssue]:
    issues: list[LintIssue] = []
    style_lower = prompt.style.casefold()
    lyrics_lower = prompt.lyrics.casefold()
    exclude_lower = prompt.exclude.casefold()
    segments = style_segments(prompt.style)
    required_segments = ["genre", "vocal", "instrumentation", "production", "mood"]
    missing_segments = [segment for segment in required_segments if segment not in segments or len(segments[segment]) < 8]
    if missing_segments:
        issues.append(LintIssue("style_too_generic", "blocker", f"Style is missing specific five-part segment(s): {', '.join(missing_segments)}.", "Use genre + vocal + instrumentation + production + mood."))
    generic_terms = re.findall(r"\b(good|nice|beautiful|emotional|vibe|catchy|pop)\b", style_lower)
    if len(generic_terms) >= 4:
        issues.append(LintIssue("style_too_generic", "warning", "Style relies on generic descriptors without enough audible detail.", "Replace vague adjectives with audible instruments, production texture, groove, and vocal behavior."))
    genre_terms = re.findall(r"\b(pop|rock|r&b|edm|folk|trap|hip hop|city pop|lo-fi|jazz|metal|punk|country|reggae)\b", style_lower)
    if len(set(genre_terms)) > 4:
        issues.append(LintIssue("genre_soup", "warning", "Too many genre labels may cause drift.", "Pick one primary lane and one secondary color."))
    if re.search(r"in the style of|像.*(周杰伦|林俊杰|Taylor|Adele|Billie|Eason|陈奕迅|王菲)|周杰伦|林俊杰|Taylor Swift|Adele|Billie Eilish|陈奕迅|王菲", prompt.style, re.I):
        issues.append(LintIssue("artist_name_risk", "blocker", "Style appears to include artist or protected identity language.", "Convert the reference into neutral style DNA."))
    if re.search(r"(模仿|克隆|copy|imitate).*(声线|声音|voice|vocal)|reference singer voice", prompt.style, re.I):
        issues.append(LintIssue("voice_imitation_risk", "blocker", "Prompt appears to ask for voice imitation.", "Describe vocal register and performance, not a specific person's voice."))
    for term in ["piano", "guitar", "bass", "drums", "synth", "rap", "钢琴", "吉他", "贝斯", "鼓", "合成器"]:
        if term in style_lower and term in exclude_lower:
            issues.append(LintIssue("exclude_conflict", "blocker", f"Exclude conflicts with required style term: {term}.", "Remove the conflict or rewrite the style requirement."))
            break
    lyric_lines = [line for line in prompt.lyrics.splitlines() if line.strip() and not line.strip().startswith("[")]
    if len(lyric_lines) < 8:
        issues.append(LintIssue("lyrics_too_short", "blocker", "Lyrics are too short for a full vocal generation.", "Add verse, chorus, and a final payoff."))
    dense_lines = [line for line in lyric_lines if len(line) > 48]
    if len(dense_lines) >= 3:
        issues.append(LintIssue("lyrics_too_dense", "warning", "Several lyric lines are too dense for natural singing.", "Shorten long lines or split phrases."))
    if prompt.title.casefold() not in lyrics_lower and prompt.title not in prompt.lyrics:
        issues.append(LintIssue("missing_hook", "warning", "Title phrase does not appear in the lyrics.", "Place the title phrase in the chorus hook."))
    if "vocal:" not in style_lower:
        issues.append(LintIssue("missing_vocal_identity", "blocker", "Style lacks an explicit vocal identity segment.", "Add vocal register, diction, breath, and performance behavior."))
    if "production:" not in style_lower:
        issues.append(LintIssue("missing_production_detail", "blocker", "Style lacks production detail.", "Add mix texture, space, width, dynamics, or specific sonic handling."))
    if len(re.findall(r"^\[[^\]]+\]", prompt.lyrics, re.M)) > 9:
        issues.append(LintIssue("section_tag_overload", "warning", "Too many section tags can overconstrain Suno.", "Keep essential sections and let arrangement breathe."))
    if route.decision == "blocked_reference_upload_use_listen_only_dna" and re.search(r"song title|artist|reference singer|原曲|歌手|歌名", prompt.style, re.I):
        issues.append(LintIssue("reference_identity_leak", "blocker", "Unsafe reference identity leaked into style prompt.", "Use only neutral DNA fields."))
    if route.decision.startswith("ordinary") and re.search(r"Audio Influence:\s*[1-9]", prompt.model_settings, re.I):
        issues.append(LintIssue("slider_conflict", "warning", "Ordinary generation should not use nonzero Audio Influence.", "Keep Audio Influence at 0 unless a seed route is approved."))
    return issues


def feedback_to_variable(feedback: str) -> str:
    text = feedback.casefold()
    routes = [
        (r"人声|声线|vocal|voice|唱", "vocal_identity"),
        (r"副歌|chorus|hook|钩子|记不住", "topline_or_chorus_lift"),
        (r"歌词|lyric|词|空泛|cliche", "lyrics_context"),
        (r"律动|groove|节奏|鼓|贝斯|bass|drum", "groove"),
        (r"结构|转场|bridge|verse|pre-chorus|intro|outro", "structure"),
        (r"混音|糊|刺耳|sibilance|mix|muddy", "production_or_exclude"),
        (r"像|太像|reference|参考|相似", "reference_distance"),
    ]
    for pattern, variable in routes:
        if re.search(pattern, text, re.I):
            return variable
    return "style_specificity"


def build_take_review(feedback: str, route: RoutePlan) -> TakeReview:
    next_variable = feedback_to_variable(feedback or route.next_action)
    fields = {
        "first_20s": "must establish title, singer identity, and arrangement world quickly",
        "hook_memory": "review whether the chorus title phrase is remembered after one listen",
        "vocal_identity": "specific human-feeling register and diction, no anonymous AI voice",
        "lyrics_intelligibility": "lines should be singable and emotionally concrete",
        "groove": "pocket should feel played, not mechanically looped",
        "chorus_lift": "chorus should open energy without overstuffing",
        "arrangement": "instrument roles should leave vocal space",
        "ai_artifacts": "watch robotic vibrato, mushy consonants, abrupt endings, muddy low end",
        "fit_to_brief": "score against the brief, not against cloning a reference",
        "similarity_risk": "low for ordinary text-only; actively check if reference DNA or upload route is used",
        "user_preference": "record what the user actually likes, not just what aligns technically",
        "next_action": f"change one primary variable next: {next_variable}",
    }
    return TakeReview(fields=fields, next_changed_variable=next_variable)


def build_preference_profile(brief: str, feedback: str) -> UserPreferenceProfile:
    language = detect_language(brief)
    fields = {
        "default_language": f"{language} (provisional unless user confirms stable default)",
        "default_use_case": infer_use_case(brief),
        "default_batch_size": "2",
        "preferred_genres": infer_genre(brief),
        "preferred_vocals": infer_vocal(brief),
        "avoid_styles": "random rap verse, robotic vocal, muddy mix, harsh sibilance, abrupt ending",
        "default_exclude": "robotic vocal, muddy mix, harsh sibilance, abrupt ending, random rap verse",
        "rights_default": "noncommercial demo unless the user states release/client use",
        "project_mode_default": "quick_idea unless release/client/catalog/rights-sensitive intent appears",
        "successful_patterns": "record only after user selects a take or repeats preference across sessions",
        "rejected_patterns": normalize(feedback) if feedback else "record only after user explicitly rejects a take or pattern",
    }
    return UserPreferenceProfile(fields=fields)


def build_effect_first_run(project_root: Path | None, brief: str, feedback: str) -> EffectFirstRun:
    route = plan_generation_route(project_root, brief)
    reference_dna = build_reference_dna(brief, route)
    prompt = build_prompt_package(brief, route, reference_dna)
    lint_issues = lint_prompt(prompt, route)
    take_review = build_take_review(feedback, route)
    preference = build_preference_profile(brief, feedback)
    return EffectFirstRun(route=route, prompt=prompt, reference_dna=reference_dna, lint_issues=lint_issues, take_review=take_review, preference=preference, brief=brief, feedback=feedback)


def finding_table(issues: list[LintIssue]) -> str:
    rows = ["| code | severity | message | suggested fix |", "|---|---|---|---|"]
    if not issues:
        rows.append("| none | pass | no linter issues | proceed to confirmation card |")
        return "\n".join(rows)
    for issue in issues:
        rows.append(f"| {issue.code} | {issue.severity} | {issue.message} | {issue.suggested_fix} |")
    return "\n".join(rows)


def dict_lines(items: dict[str, str]) -> str:
    return "\n".join(f"- {key}: {value}" for key, value in items.items())


