def write_if_allowed(path: Path, text: str, allow_overwrite: bool) -> None:
    if path.exists() and not allow_overwrite:
        raise FileExistsError(f"output exists: {path}")
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8")


def outputs(run: EffectFirstRun, project_root: Path | None) -> dict[str, str]:
    return {
        OUTPUT: render_main(run, project_root),
        PROMPT_OUTPUT: render_prompt_package(run),
        LINTER_OUTPUT: render_linter(run),
        REFERENCE_DNA_OUTPUT: render_reference_dna(run.reference_dna),
        TAKE_REVIEW_OUTPUT: render_take_review(run),
        PREFERENCE_OUTPUT: render_preference(run.preference),
    }


def parse_args(argv: list[str]) -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--project-root", help="Optional song project root.")
    parser.add_argument("--brief", default="", help="User brief.")
    parser.add_argument("--brief-file", help="Read additional brief text from a file.")
    parser.add_argument("--feedback", default="", help="Optional take feedback to map to the next variable.")
    parser.add_argument("--write", action="store_true", help="Write effect-first files into the project.")
    parser.add_argument("--allow-overwrite", action="store_true", help="Allow overwriting output files.")
    return parser.parse_args(argv)


def main(argv: list[str]) -> int:
    args = parse_args(argv)
    project_root = Path(args.project_root).expanduser().resolve() if args.project_root else None
    if project_root and not project_root.exists():
        print(f"error: project root does not exist: {project_root}", file=sys.stderr)
        return 2
    brief = args.brief
    if args.brief_file:
        brief_path = Path(args.brief_file).expanduser().resolve()
        if not brief_path.exists():
            print(f"error: brief file does not exist: {brief_path}", file=sys.stderr)
            return 2
        brief = "\n".join(part for part in [brief, read_text(brief_path)] if part)
    if project_root and not brief:
        brief = "\n".join(
            read_text(project_root / rel)
            for rel in ["01_brief/song-brief.md", "01_brief/project-intake.md", "00_admin/generation-route-plan.md"]
            if (project_root / rel).exists()
        )
    if not brief:
        print("error: provide --brief, --brief-file, or a project with brief files", file=sys.stderr)
        return 2
    run = build_effect_first_run(project_root, brief, args.feedback)
    rendered = outputs(run, project_root)
    if args.write:
        if not project_root:
            print("error: --write requires --project-root", file=sys.stderr)
            return 2
        try:
            for rel, text in rendered.items():
                write_if_allowed(project_root / rel, text, args.allow_overwrite)
        except FileExistsError as exc:
            print(f"error: {exc}", file=sys.stderr)
            return 2
        for rel in rendered:
            print(f"wrote {rel}")
    else:
        print(rendered[OUTPUT])
    return 0


if __name__ == "__main__":
    raise SystemExit(main(sys.argv[1:]))
