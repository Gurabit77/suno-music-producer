def methodology_transfer_audit_ready(project_root: Path) -> bool:
    text = (project_root / DEFAULT_OUTPUT).read_text(encoding="utf-8") if (project_root / DEFAULT_OUTPUT).exists() else ""
    lowered = text.casefold()
    return (
        MARKER.casefold() in lowered
        and "decision: pass" in lowered
        and "## methodology transfer evidence" in lowered
        and "local source status: ready" in lowered
        and "external method claims: pass" in lowered
        and "source confidence split:" in lowered
        and "official control surfaces:" in lowered
        and "model/version rationale:" in lowered
        and "primary tool route:" in lowered
        and "human-likeness transfer:" in lowered
        and "variable discipline:" in lowered
        and "anti-magic safeguards:" in lowered
    )


def write_if_allowed(path: Path, text: str, allow_overwrite: bool) -> None:
    if path.exists() and not allow_overwrite:
        raise FileExistsError(path)
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8")


def parse_args(argv: list[str]) -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--project-root", required=True, help="Song project root created by create_music_song_project.py.")
    parser.add_argument("--source-root", default=str(DEFAULT_SOURCE_ROOT), help="Local Suno methodology source folder.")
    parser.add_argument("--output", default=DEFAULT_OUTPUT, help="Relative output path for the audit.")
    parser.add_argument("--write", action="store_true", help="Write the audit file.")
    parser.add_argument("--allow-overwrite", action="store_true", help="Allow overwriting an existing audit.")
    parser.add_argument("--strict", action="store_true", help="Return exit code 1 when blockers remain.")
    return parser.parse_args(argv)


def main(argv: list[str]) -> int:
    args = parse_args(argv)
    project_root = Path(args.project_root).expanduser().resolve()
    source_root = Path(args.source_root).expanduser().resolve()
    if not project_root.exists() or not project_root.is_dir():
        print(f"error: project root not found: {project_root}", file=sys.stderr)
        return 2

    docs = read_project(project_root)
    fields, findings, local_ready, optional_ready = analyze(project_root, docs, source_root)
    audit = render_audit(project_root, source_root, fields, findings, local_ready, optional_ready)
    blockers = [finding for finding in findings if finding.severity == "blocker"]

    if args.write:
        try:
            write_if_allowed(project_root / args.output, audit, args.allow_overwrite)
        except FileExistsError as exc:
            print(f"error: output exists, use --allow-overwrite: {exc}", file=sys.stderr)
            return 2
    else:
        print(audit)

    if blockers:
        print(f"methodology transfer has {len(blockers)} blocker(s).", file=sys.stderr)
        for item in blockers:
            print(f"- {item.code}: {item.message} -> {item.route}", file=sys.stderr)
        return 1 if args.strict else 0

    print("methodology transfer approved.")
    print(f"audit: {project_root / args.output}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main(sys.argv[1:]))
