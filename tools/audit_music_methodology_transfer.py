def read_project(project_root: Path) -> dict[str, str]:
    docs: dict[str, str] = {}
    for path in project_root.rglob("*.md"):
        rel = path.relative_to(project_root).as_posix()
        try:
            docs[rel] = path.read_text(encoding="utf-8")
        except UnicodeDecodeError:
            docs[rel] = path.read_text(errors="replace")
    return docs


def normalize_value(raw: str) -> str:
    value = raw.strip().strip("`")
    if (value.startswith('"') and value.endswith('"')) or (value.startswith("'") and value.endswith("'")):
        value = value[1:-1].strip()
    value = re.sub(r"\s+", " ", value)
    if value.casefold() in EMPTY_VALUES:
        return ""
    if value.startswith("[") and value.endswith("]") and ":" in value:
        return ""
    return value


def scan_key_values(text: str) -> list[tuple[str, str]]:
    pairs: list[tuple[str, str]] = []
    for line in text.splitlines():
        stripped = line.strip()
        if not stripped or stripped.startswith("#") or stripped.startswith("|"):
            continue
        stripped = re.sub(r"^[-*]\s+", "", stripped)
        match = re.match(r"^([^:]{1,110}):\s*(.*)$", stripped)
        if not match:
            continue
        label = re.sub(r"\s+", " ", match.group(1).strip()).casefold()
        value = normalize_value(match.group(2))
        pairs.append((label, value))
    return pairs


def extract_value(docs: dict[str, str], paths: list[str], labels: list[str]) -> str:
    wanted = {re.sub(r"\s+", " ", label.strip()).casefold() for label in labels}
    for rel in paths:
        for label, value in scan_key_values(docs.get(rel, "")):
            if label in wanted and value:
                return value
    return ""


def positive_value(value: str) -> bool:
    return bool(re.search(r"\b(yes|enabled|on|true|use|active)\b|开启|使用|启用", value, re.I))


def protected_identity_hits(text: str) -> list[str]:
    hits: list[str] = []
    for name in PROTECTED_IDENTITY_NAMES:
        if re.fullmatch(r"[A-Za-z0-9 .'\-&]+", name):
            pattern = rf"(?<![A-Za-z0-9]){re.escape(name)}(?![A-Za-z0-9])"
            found = re.search(pattern, text, flags=re.I)
        else:
            found = name.casefold() in text.casefold()
        if found:
            hits.append(name)
    return hits


def unresolved_protected_identity(text: str) -> list[str]:
    hits: list[str] = []
    lowered = text.casefold()
    for name in protected_identity_hits(text):
        pattern = re.escape(name)
        match = re.search(pattern, text, flags=re.I)
        window = ""
        if match:
            start = max(0, match.start() - 80)
            end = min(len(text), match.end() + 80)
            window = text[start:end].casefold()
        safe_context = bool(
            re.search(
                r"remove|removed|reject|rejected|not use|do not use|avoid|forbidden|neutral|style dna|去身份|移除|拒绝|不用|禁止|中性|抽象",
                window or lowered,
                re.I,
            )
        )
        if not safe_context:
            hits.append(name)
    return hits


def surfaces_from_text(text: str) -> list[str]:
    found: list[str] = []
    for key, pattern in OFFICIAL_SURFACE_PATTERNS.items():
        if re.search(pattern, text, re.I):
            found.append(key)
    return found


def term_is_negated(text: str, pattern: str) -> bool:
    for match in re.finditer(pattern, text, flags=re.I):
        start = max(0, match.start() - 80)
        end = min(len(text), match.end() + 80)
        window = text[start:end]
        if re.search(r"\b(no|not|without|inactive|unused|disabled|off|none|avoid|reject|rejected|do not|don't)\b|不使用|不用|未启用|关闭|拒绝|避免", window, re.I):
            return True
    return False


def active_route(text: str, pattern: str) -> bool:
    return bool(re.search(pattern, text, re.I)) and not term_is_negated(text, pattern)


def unrejected_magic_claim(text: str) -> bool:
    pattern = r"guarantee|guaranteed|secret|magic|viral|100%|必爆|神 prompt|万能|绝对"
    return active_route(text, pattern)


def has_date_like(value: str) -> bool:
    return bool(re.search(r"\b20\d{2}[-/年.]\d{1,2}([-/.月]\d{1,2})?\b|最近|本轮|current|latest|二次检索", value, re.I))


def check_source_root(source_root: Path) -> tuple[list[Finding], list[str], list[str]]:
    findings: list[Finding] = []
    ready: list[str] = []
    optional_ready: list[str] = []

    if not source_root.exists() or not source_root.is_dir():
        findings.append(
            Finding(
                "blocker",
                "local_method_source_root_missing",
                f"local Suno methodology source root does not exist: {source_root}",
                "restore /path/to/suno-prompt-methodology or pass --source-root",
            )
        )
        return findings, ready, optional_ready

    for rel in REQUIRED_LOCAL_SOURCE_RELS:
        path = source_root / rel
        if path.exists():
            ready.append(rel)
        else:
            findings.append(
                Finding(
                    "blocker",
                    "local_method_source_missing",
                    f"local methodology source is not available: {rel}",
                    "refresh the local Suno prompt methodology source folder",
                )
            )

    for rel in OPTIONAL_HIGH_VALUE_RELS:
        if (source_root / rel).exists():
            optional_ready.append(rel)

    if len(optional_ready) < 3:
        findings.append(
            Finding(
                "warning",
                "local_method_optional_sources_thin",
                "high-value optional methodology references are thin; model/tool route may lack local comparison context.",
                "refresh GitHub/plugin references and official clean-md snapshots when possible",
            )
        )

    return findings, ready, optional_ready


