#!/usr/bin/env python3
"""Verify the AI music repair routing CLI."""

from __future__ import annotations

import subprocess
import sys
import tempfile
from pathlib import Path


ROOT = Path("/path/to/suno-music-producer")
SCAFFOLD = ROOT / "tools" / "create_music_song_project.py"
TAKE_REVIEW = ROOT / "tools" / "review_music_takes.py"
ROUTER = ROOT / "tools" / "route_music_repairs.py"
WIKI_ARTIFACT = Path("/path/to/obsidian-vault/wiki/分析/音乐/AI 音乐 AI味伪影诊断与修复路由工作流.md")
WIKI_PROJECT = Path("/path/to/obsidian-vault/wiki/分析/音乐/AI 音乐单曲项目文件夹与证据包模板.md")
LOG = Path("/path/to/obsidian-vault/wiki/log.md")


def write(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8")


def generation_evidence_audit(project: Path) -> str:
    return f"""# Generation Evidence Audit

Generated by: tools/audit_music_generation_evidence.py
Project root: {project}
Decision: pass

## Generation Evidence

Take provenance tags: A01, A02, A03
Batch tags: candidate ids, takes per candidate, total takes, generation date, expected listen order, anonymization rule
Provider metadata tags: A01, A02, A03
Blind review handoff: ready
"""


def main() -> int:
    errors: list[str] = []
    for path in [SCAFFOLD, TAKE_REVIEW, ROUTER, WIKI_ARTIFACT, WIKI_PROJECT, LOG]:
        if not path.exists():
            errors.append(f"missing required file: {path}")
    if errors:
        print("\n".join(errors), file=sys.stderr)
        return 1

    with tempfile.TemporaryDirectory(prefix="ai-music-repair-router-") as tmp:
        tmp_path = Path(tmp)
        scaffold = subprocess.run(
            [
                sys.executable,
                str(SCAFFOLD),
                "--output-dir",
                str(tmp_path),
                "--date",
                "20260624",
                "--language",
                "zh",
                "--lane",
                "pop",
                "--slug",
                "night-bus",
                "--title",
                "Night Bus Confession",
                "--artist-project",
                "Example Test Artist",
                "--use-case",
                "demo",
            ],
            cwd=ROOT,
            text=True,
            capture_output=True,
        )
        if scaffold.returncode != 0:
            errors.append(f"scaffold failed: {scaffold.stderr.strip()}")
            print("\n".join(errors), file=sys.stderr)
            return 1

        project = tmp_path / "20260624_zh-pop_night-bus"
        empty_route = subprocess.run(
            [sys.executable, str(ROUTER), "--project-root", str(project), "--strict"],
            cwd=ROOT,
            text=True,
            capture_output=True,
        )
        if empty_route.returncode == 0:
            errors.append("strict repair router should reject missing take-selection decision")

        write(
            project / "05_generations" / "take-ledger.md",
            """# Take Ledger

| take id | prompt package | provider/model | settings | source audio | persona/voice | first impression | known issue | review status |
|---|---|---|---|---|---|---|---|---|
| A01 | 04_prompt/prompt-package-v001.md | Suno current | W25/S70/A0 | none | fictional singer | pretty but generic | weak hook | reviewed |
| A02 | 04_prompt/prompt-package-v001.md | Suno current | W25/S70/A0 | none | fictional singer | strong chorus but vocal tail is synthetic | vocal tail artifact and minor mix polish | reviewed |
| A03 | 04_prompt/prompt-package-v001.md | Suno current | W45/S55/A0 | none | fictional singer | interesting texture | AI vocal tail | reviewed |
""",
        )
        write(project / "05_generations" / "generation-evidence-audit.md", generation_evidence_audit(project))
        write(
            project / "06_review" / "blind-review.md",
            """# Blind Review

## Scores

| take | alignment | preference | hook | vocal identity | groove | form | artifact risk | decision |
|---|---:|---:|---:|---:|---:|---:|---:|---|
| A01 | 3.4 | 3.2 | 2.8 | 3.6 | 3.5 | 3.4 | 1.5 | reject |
| A02 | 4.2 | 4.0 | 4.4 | 3.4 | 4.2 | 4.1 | 2.2 | repair |
| A03 | 4.0 | 3.8 | 4.1 | 3.2 | 3.9 | 3.7 | 3.0 | repair |
""",
        )
        review = subprocess.run(
            [
                sys.executable,
                str(TAKE_REVIEW),
                "--project-root",
                str(project),
                "--write",
                "--allow-overwrite",
                "--strict",
            ],
            cwd=ROOT,
            text=True,
            capture_output=True,
        )
        if review.returncode != 0:
            errors.append(f"take review failed: stdout={review.stdout.strip()} stderr={review.stderr.strip()}")

        route = subprocess.run(
            [
                sys.executable,
                str(ROUTER),
                "--project-root",
                str(project),
                "--write",
                "--allow-overwrite",
                "--strict",
            ],
            cwd=ROOT,
            text=True,
            capture_output=True,
        )
        if route.returncode != 0:
            errors.append(f"repair router failed: stdout={route.stdout.strip()} stderr={route.stderr.strip()}")

        expected_outputs = [
            project / "07_repair" / "artifact-severity-score.md",
            project / "07_repair" / "repair-route-map.md",
            project / "07_repair" / "repair-log.md",
            project / "07_repair" / "regression-listen.md",
        ]
        for path in expected_outputs:
            if not path.exists():
                errors.append(f"missing router output: {path}")

        route_map = project / "07_repair" / "repair-route-map.md"
        if route_map.exists():
            route_text = route_map.read_text(encoding="utf-8")
            for term in [
                "Selected take: A02",
                "AI 音乐声线身份与演唱表演工作流",
                "AI 音乐 AI味伪影诊断与修复路由工作流",
                "Official Suno edit tool:",
                "Official Suno Edit Tool Routes",
                "Suno Song Editor / Replace Section or Studio stems",
                "official-song-editor.md",
                "Repair only one primary blocker",
                "tools/route_music_repairs.py",
            ]:
                if term not in route_text:
                    errors.append(f"repair route output missing term: {term}")

        artifact_score = project / "07_repair" / "artifact-severity-score.md"
        if artifact_score.exists():
            artifact_text = artifact_score.read_text(encoding="utf-8")
            for term in ["Top 3 Blockers", "yes / local repair", "Official Suno Edit Tool Routes", "Severity uses the 0-3 scale"]:
                if term not in artifact_text:
                    errors.append(f"artifact score output missing term: {term}")

        repair_log = project / "07_repair" / "repair-log.md"
        if repair_log.exists():
            log_text = repair_log.read_text(encoding="utf-8")
            for term in ["Official Suno edit tool:", "Tool action:", "Suno regression check:"]:
                if term not in log_text:
                    errors.append(f"repair log output missing term: {term}")

        regression = project / "07_repair" / "regression-listen.md"
        if regression.exists():
            regression_text = regression.read_text(encoding="utf-8")
            for term in ["Official Suno edit tool:", "Tool action used:", "Suno tool regression check:"]:
                if term not in regression_text:
                    errors.append(f"regression listen output missing term: {term}")

        no_overwrite = subprocess.run(
            [sys.executable, str(ROUTER), "--project-root", str(project), "--write"],
            cwd=ROOT,
            text=True,
            capture_output=True,
        )
        if no_overwrite.returncode != 2:
            errors.append("repair router should refuse to overwrite existing outputs without --allow-overwrite")

    artifact_page = WIKI_ARTIFACT.read_text(encoding="utf-8")
    project_page = WIKI_PROJECT.read_text(encoding="utf-8")
    log_text = LOG.read_text(encoding="utf-8")
    for term in [
        "tools/route_music_repairs.py",
        "artifact-severity-score.md",
        "repair-route-map.md",
        "regression-listen.md",
        "--strict",
    ]:
        if term not in artifact_page:
            errors.append(f"artifact workflow missing router term: {term}")
    for term in ["tools/route_music_repairs.py", "repair-route-map.md", "take-selection-decision.md", "official Suno edit tool"]:
        if term not in project_page:
            errors.append(f"song project template missing router term: {term}")
    if "AI 音乐修复路由工具" not in log_text:
        errors.append("wiki log missing repair router entry")

    if errors:
        print("AI music repair router verification failed:", file=sys.stderr)
        for error in errors:
            print(f"- {error}", file=sys.stderr)
        return 1

    print("AI music repair router verification passed.")
    print(f"router: {ROUTER}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
