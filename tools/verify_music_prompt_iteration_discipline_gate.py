def main() -> int:
    errors: list[str] = []
    for path in [SCAFFOLD, TOOL, WIKI_EXPERIMENT, WIKI_TEMPLATE, WIKI_WORKFLOW]:
        if not path.exists():
            errors.append(f"missing required file: {path}")
    if errors:
        print("\n".join(errors), file=sys.stderr)
        return 1

    with tempfile.TemporaryDirectory(prefix="ai-music-prompt-iteration-") as tmp:
        project = scaffold_project(Path(tmp))

        empty = run([sys.executable, str(TOOL), "--project-root", str(project), "--strict"])
        if empty.returncode == 0:
            errors.append("empty prompt iteration discipline audit should fail in strict mode")
        empty_text = empty.stdout + "\n" + empty.stderr
        for term in ["prompt_package_not_approved", "experiment_question_missing", "changed_variables_missing"]:
            if term not in empty_text:
                errors.append(f"empty strict failure missing term: {term}")

        write_good_discipline(project, changed_variables="Style prompt, Lyrics text, Exclude, Weirdness", phase="convergence")
        too_many = run([sys.executable, str(TOOL), "--project-root", str(project), "--strict"])
        if too_many.returncode == 0:
            errors.append("convergence prompt iteration audit should reject too many changed variables")
        too_many_text = too_many.stdout + "\n" + too_many.stderr
        if "too_many_changed_variables" not in too_many_text:
            errors.append("too-many-vars blocker missing too_many_changed_variables")

        write_good_discipline(project, baseline=False)
        no_baseline = run([sys.executable, str(TOOL), "--project-root", str(project), "--strict"])
        if no_baseline.returncode == 0:
            errors.append("prompt iteration audit should reject a candidate set without baseline")
        no_baseline_text = no_baseline.stdout + "\n" + no_baseline.stderr
        if "baseline_candidate_missing" not in no_baseline_text:
            errors.append("no-baseline blocker missing baseline_candidate_missing")

        write_good_discipline(project)
        ok = run(
            [
                sys.executable,
                str(TOOL),
                "--project-root",
                str(project),
                "--write",
                "--allow-overwrite",
                "--strict",
            ]
        )
        if ok.returncode != 0:
            errors.append(f"prompt iteration discipline audit should pass: {ok.stderr.strip()}")
        output = project / "05_generations" / "prompt-iteration-discipline-audit.md"
        if not output.is_file():
            errors.append("missing prompt iteration discipline audit output")
        else:
            text = output.read_text(encoding="utf-8")
            for term in [
                "Decision: pass",
                "tools/audit_music_prompt_iteration_discipline.py",
                "Prompt Iteration Discipline Evidence",
                "Experiment question:",
                "Frozen brief: complete",
                "Allowed variable count: 1",
                "Changed variables: style influence",
                "Candidate hypotheses: present",
                "Batch discipline:",
                "Attribution strength: strong",
                "Generation evidence handoff: pass",
            ]:
                if term not in text:
                    errors.append(f"prompt iteration audit output missing term: {term}")

        no_overwrite = run([sys.executable, str(TOOL), "--project-root", str(project), "--write", "--strict"])
        if no_overwrite.returncode == 0:
            errors.append("prompt iteration discipline audit should refuse overwrite without --allow-overwrite")

    for page, terms in [
        (
            WIKI_EXPERIMENT,
            [
                "tools/audit_music_prompt_iteration_discipline.py",
                "prompt-iteration-discipline-audit.md",
                "prompt-iteration-discipline-gate",
                "Changed variables",
                "Attribution strength",
            ],
        ),
        (
            WIKI_TEMPLATE,
            [
                "experiment-intent.md",
                "brief-freeze.md",
                "variable-inventory.md",
                "prompt-candidate-set.md",
                "prompt-iteration-discipline-audit.md",
                "tools/audit_music_prompt_iteration_discipline.py",
            ],
        ),
        (
            WIKI_WORKFLOW,
            [
                "prompt-iteration-discipline-gate",
                "tools/audit_music_prompt_iteration_discipline.py",
                "prompt-experiment-gate",
            ],
        ),
    ]:
        text = page.read_text(encoding="utf-8")
        for term in terms:
            if term not in text:
                errors.append(f"{page.name} missing term: {term}")

    log_text = LOG.read_text(encoding="utf-8") if LOG.exists() else ""
    if "AI 音乐 Prompt Iteration Discipline 工具闸门" not in log_text:
        errors.append("wiki log missing prompt iteration discipline tool entry")

    if errors:
        print("AI music prompt iteration discipline verification failed:", file=sys.stderr)
        for error in errors:
            print(f"- {error}", file=sys.stderr)
        return 1

    print("AI music prompt iteration discipline verification passed.")
    print(f"script: {TOOL}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
