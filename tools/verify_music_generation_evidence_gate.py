#!/usr/bin/env python3
"""Verify the AI music generation evidence gate."""

from __future__ import annotations

import subprocess
import sys
import tempfile
from pathlib import Path


ROOT = Path("/path/to/suno-music-producer")
SCAFFOLD = ROOT / "tools" / "create_music_song_project.py"
TOOL = ROOT / "tools" / "audit_music_generation_evidence.py"
WIKI_EXPERIMENT = Path("/path/to/obsidian-vault/wiki/分析/音乐/AI 音乐 Prompt 实验矩阵与版本收敛工作流.md")
WIKI_REVIEW = Path("/path/to/obsidian-vault/wiki/分析/音乐/AI 音乐盲听评审与版本归档工作流.md")
WIKI_PROJECT = Path("/path/to/obsidian-vault/wiki/分析/音乐/AI 音乐单曲项目文件夹与证据包模板.md")
LOG = Path("/path/to/obsidian-vault/wiki/log.md")


def run(cmd: list[str]) -> subprocess.CompletedProcess[str]:
    return subprocess.run(cmd, cwd=ROOT, text=True, capture_output=True)


def write(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8")


