def repair_route_ready(project_root: Path) -> bool:
    text = read_text(project_root / "07_repair/repair-route-map.md")
    for table in parse_markdown_tables(text):
        headers = set(table[0].keys()) if table else set()
        if "problem" not in headers or "layer" not in headers:
            continue
        for row in table:
            problem = row.get("problem", "").strip().lower()
            if problem and problem != "problem":
                return True
    return False


