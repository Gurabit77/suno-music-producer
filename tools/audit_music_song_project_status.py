def yes_no_pass(value: str, *, allow_na: bool = True) -> bool:
    normalized = value.strip().lower()
    if allow_na and normalized in {"n/a", "na", "not applicable"}:
        return True
    return bool(re.search(r"^(yes|y|pass|passed|preserved|no new|none|no)$", normalized, re.I))


def repair_regression_ready(project_root: Path) -> bool:
    text = read_text(project_root / "07_repair/regression-listen.md")
    if not text:
        return False
    decision = field_value(text, ["Decision"])
    if not re.search(r"keep|pass|passed|resolved|approve", decision, re.I):
        return False
    improvement = field_value(text, ["Did issue improve? 0-3", "Did issue improve"])
    match = re.search(r"(\d+(?:\.\d+)?)", improvement)
    if not match or float(match.group(1)) < 2.0:
        return False
    new_artifacts = field_value(text, ["New artifacts introduced"])
    if not new_artifacts or not re.search(r"^(no|none|n/a|na)$", new_artifacts.strip(), re.I):
        return False
    for label in ["Hook preserved", "Vocal identity preserved", "Groove preserved"]:
        value = field_value(text, [label])
        if not value or not yes_no_pass(value):
            return False
    return True


