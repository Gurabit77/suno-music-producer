def release_gate_blocker(project_root: Path) -> str:
    text = read_text(project_root / "11_release/release-candidate-rights-gate.md")
    if not text:
        return "release-candidate rights gate is missing"
    if RELEASE_GATE_MARKER not in text:
        return "release-candidate rights gate is not generated by tools/prepare_music_release_candidate.py"
    if not decision_is(text, r"approve release-candidate"):
        return "release-candidate rights gate is not approved"
    if not release_pack_ready(project_root):
        return "release gate is approved but release-candidate audit/package/manifest are missing, hold, or not tool-generated"
    return ""


