def release_audio_qc_report_ready(text: str) -> bool:
    if RELEASE_AUDIO_QC_MARKER not in text:
        return False
    if not decision_is(text, r"pass|approved|approve"):
        return False
    lowered = text.lower()
    if any(term not in lowered for term in RELEASE_AUDIO_QC_REQUIRED_TERMS):
        return False
    if re.search(r"Decision:\s*(hold|fail|return|do not release)|\b(return to master|return to mix|return to daw|do not release)\b", text, re.I):
        return False
    return True


def audio_technical_report_ready(text: str) -> bool:
    if AUDIO_INSPECTION_MARKER not in text:
        return False
    if not decision_is(text, r"pass|approved|approve"):
        return False
    lowered = text.lower()
    if "## results" not in lowered:
        return False
    if any(metric not in lowered for metric in AUDIO_INSPECTION_METRICS):
        return False
    table_rows = [line.strip() for line in text.splitlines() if line.strip().startswith("|")]
    data_rows = [
        row
        for row in table_rows
        if row.count("|") >= 10 and not re.search(r"^\|\s*-", row) and "sample rate" not in row.lower()
    ]
    return any(re.search(r"\|\s*pass\s*\|\s*$", row, re.I) for row in data_rows)


