def read_text(path: Path) -> str:
    return path.read_text(encoding="utf-8") if path.exists() else ""


def field_value(text: str, labels: list[str]) -> str:
    wanted = {label.lower() for label in labels}
    for line in text.splitlines():
        stripped = re.sub(r"^[-*]\s+", "", line.strip())
        match = re.match(r"^([^:]{1,80}):\s*(.*)$", stripped)
        if not match:
            continue
        label = match.group(1).strip().lower()
        value = match.group(2).strip().strip('"')
        if label in wanted and value:
            return value
    return ""


def value_from_files(project_root: Path, rels: list[str], labels: list[str]) -> str:
    for rel in rels:
        value = field_value(read_text(project_root / rel), labels)
        if value:
            return value
    return ""


def command(script: str, project_root: Path) -> str:
    return f"python3 tools/{script} --project-root {project_root} --write --allow-overwrite --strict"


def decision_is(text: str, pattern: str) -> bool:
    decision = field_value(text, ["Decision"])
    return bool(decision and re.search(pattern, decision, re.I))


def prompt_package_ready(project_root: Path) -> bool:
    text = read_text(project_root / "04_prompt/prompt-package-v001.md")
    if re.search(r"Decision:\s*repair before generation|NEEDS_REPAIR", text, re.I):
        return False
    lowered = text.lower()
    return (
        PROMPT_COMPILER_MARKER in text
        and decision_is(text, r"compile approved")
        and all(term in lowered for term in PROMPT_PACKAGE_REQUIRED_TERMS)
    )


def prompt_preflight_ready(project_root: Path) -> bool:
    text = read_text(project_root / "04_prompt/prompt-preflight-review.md")
    if not text:
        return False
    if PROMPT_COMPILER_MARKER not in text:
        return False
    lowered = text.lower()
    if any(term not in lowered for term in PROMPT_PREFLIGHT_REQUIRED_TERMS):
        return False
    if re.search(
        r"repair before generation|Field completeness:\s*fail|Prompt specificity:\s*(fail|review)|Genre/lane authenticity:\s*(fail|review)|Lyric narrative:\s*(fail|review)|Reference audit:\s*(fail|review)|Chord skeleton transfer:\s*fail|Prosody audit:\s*(fail|review)|Topline audit:\s*(fail|review)|Harmony/Bass audit:\s*(fail|review)|Groove audit:\s*(fail|review)|Structure audit:\s*(fail|review)|Vocal audit:\s*(fail|review)|Human anchor lane:\s*fail|Identity routing:\s*fail|My Taste / boost:\s*fail|Identity / rights:\s*fail|Conflict check:\s*fail|Slider intent:\s*(fail|review)|Model/version:\s*fail",
        text,
        re.I,
    ):
        return False
    return decision_is(text, r"compile approved|approved|pass")


def take_decision_ready(project_root: Path) -> bool:
    text = read_text(project_root / "06_review/take-selection-decision.md")
    best = field_value(text, ["Best take"])
    action = field_value(text, ["Action"])
    if not best or not action:
        return False
    if re.search(r"repair review evidence|insufficient evidence", action, re.I):
        return False
    return True


