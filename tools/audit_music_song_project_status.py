def decide_stage(project_root: Path) -> StageState:
    if not methodology_transfer_audit_ready(project_root):
        return StageState(
            current_stage="methodology-transfer-gate",
            status="design",
            current_blocker="methodology transfer audit is missing or not passed",
            next_action="source-rank external method claims, then map local local Suno prompt methodology, official control surfaces, community candidates, model/tool route, variable discipline, and anti-magic safeguards to this song",
            next_command=command("audit_music_methodology_transfer.py", project_root),
            why="generation should not start until prompt methods and web/community claims are tied to this song's human-likeness target and treated as evidence rather than magic tags",
            do_not_do="do not compile from community tips, artist names, or model-version habits that are not source-ranked and song-specific",
        )
    if not prompt_specificity_audit_ready(project_root):
        return StageState(
            current_stage="prompt-specificity-gate",
            status="design",
            current_blocker="prompt specificity budget audit is missing or not passed",
            next_action="translate vague style words into recording, performance, section, and field-budget evidence",
            next_command=command("audit_music_prompt_specificity_budget.py", project_root),
            why="generation should not start until the prompt is specific enough to be blind-testable and free of vague style tags",
            do_not_do="do not compile from generic labels such as realistic, emotional, professional, catchy, or high quality",
        )
    if not genre_lane_authenticity_audit_ready(project_root):
        return StageState(
            current_stage="genre-lane-authenticity-gate",
            status="design",
            current_blocker="genre/lane authenticity audit is missing or not passed",
            next_action="prove the style fields cohere as a real language/genre lane with scene, tempo, vocal, arrangement, production, lyric stance, hook, and anti-drift evidence",
            next_command=command("audit_music_genre_lane_authenticity.py", project_root),
            why="generation should not start until concrete prompt fields form a believable production lane instead of genre soup or style-lyric mismatch",
            do_not_do="do not compile from stacked genre tags whose blend, lyric stance, arrangement, or production grammar is not explained",
        )
    if not lyric_narrative_audit_ready(project_root):
        return StageState(
            current_stage="lyric-narrative-gate",
            status="design",
            current_blocker="lyric narrative/cliche-cut audit is missing or not passed",
            next_action="prove narrator, concrete scene, image bank, title phrase function, section information, Verse 2 development, bridge/payoff turn, and cliche replacements",
            next_command=command("audit_music_lyric_narrative.py", project_root),
            why="generation should not start until lyrics have semantic evidence of a human narrator, concrete images, section development, and removed AI-cliche language",
            do_not_do="do not treat prosody-only lyrics as semantically human or generate from abstract slogan choruses",
        )
    if chord_skeleton_transfer_required(project_root) and not chord_skeleton_transfer_audit_ready(project_root):
        return StageState(
            current_stage="chord-skeleton-transfer-gate",
            status="design",
            current_blocker="reference upload / chord skeleton route is active but chord skeleton transfer audit is missing or not passed",
            next_action="prove reference status, allowed action, BPM/Key/progression, MIDI simplification, upload permission, retention target, prompt pairing, and reverse-analysis plan",
            next_command=command("audit_music_chord_skeleton_transfer.py", project_root),
            why="reference upload generation must prove it is using a self-owned or authorized chord skeleton, not an unauthorized reference clone",
            do_not_do="do not upload reference audio, exported MIDI, or a chord skeleton into Suno before this gate passes",
        )
    if not prompt_package_ready(project_root) or not prompt_preflight_ready(project_root):
        return StageState(
            current_stage="prompt-compile-gate",
            status="design",
            current_blocker="compiler-generated prompt package or prompt preflight is missing, unresolved, or not approved",
            next_action="fill brief/writing/prompt inputs, then run the prompt package compiler",
            next_command=command("compile_music_prompt_package.py", project_root),
            why="generation should not start until Style, Lyrics, Exclude, sliders, and rights constraints are explicit and compiler-audited",
            do_not_do="do not batch-generate from an empty scaffold or vague genre labels",
        )
    if not prompt_iteration_discipline_audit_ready(project_root):
        return StageState(
            current_stage="prompt-iteration-discipline-gate",
            status="prompt compiled",
            current_blocker="prompt iteration discipline audit is missing or not passed",
            next_action="freeze the brief, state the experiment question, list allowed variables, write prompt candidates, and prove the batch changes only the allowed variable(s)",
            next_command=command("audit_music_prompt_iteration_discipline.py", project_root),
            why="generated takes must be attributable before they can teach prompt memory or enter blind review",
            do_not_do="do not spend generation attempts or audit take provenance from a batch that changed style, lyrics, sliders, persona, and excludes at once",
        )
    if not generation_evidence_ready(project_root):
        return StageState(
            current_stage="prompt-experiment-gate",
            status="prompt compiled",
            current_blocker="generation evidence audit is missing or not passed",
            next_action="fill batch-generation-plan, take-ledger, provider-result-ledger, metadata, and audio paths or links, then audit generation evidence",
            next_command=command("audit_music_generation_evidence.py", project_root),
            why="generated takes must prove prompt package, provider/model, settings, audio source, and metadata provenance before blind review",
            do_not_do="do not blind-review, lock, repair, DAW-handoff, release, or catalog orphan takes",
        )
    if audio_seed_required(project_root) and not audio_seed_audit_ready(project_root):
        return StageState(
            current_stage="audio-seed-retention-gate",
            status="prompt experiment",
            current_blocker="human audio seed is used but audio seed retention audit is missing or not passed",
            next_action="fill audio-rights-ledger, retention-target, prompt-pairing, generation evidence, and anchor-retention-review, then audit seed retention",
            next_command=command("audit_music_audio_seed_retention.py", project_root),
            why="a generated take that used Audio Upload, Cover, Add Vocals, or nonzero Audio Influence must prove the intended human seed survived before ordinary blind review",
            do_not_do="do not treat a seeded generation as best take, release candidate, or catalog seed until retention and rights evidence pass",
        )
    if not take_decision_ready(project_root):
        return StageState(
            current_stage="take-selection-gate",
            status="prompt experiment",
            current_blocker="no scored best-take decision found",
            next_action="fill take-ledger and blind-review scores, then select the next production action",
            next_command=command("review_music_takes.py", project_root),
            why="the workflow needs a blind-review decision before repair, DAW, or release routing",
            do_not_do="do not pick the loudest or newest take without structured listening evidence",
        )
    if not repair_route_ready(project_root):
        return StageState(
            current_stage="repair-routing-gate",
            status="best take",
            current_blocker="best take exists but no concrete repair route rows are present",
            next_action="turn the take decision into repair routes and regression checks",
            next_command=command("route_music_repairs.py", project_root),
            why="AI flavor must be assigned to lyric, prosody, topline, groove, vocal, mix, rights, or catalog layers",
            do_not_do="do not keep regenerating the whole song before identifying the blocker layer",
        )
    action = take_action(project_root)
    if action in {"controlled regeneration", "discard and repair brief"}:
        return StageState(
            current_stage="prompt-experiment-gate",
            status="repair required",
            current_blocker=f"take action is {action}; this is not a post-production or release candidate",
            next_action="repair the brief or run a single-variable generation batch before selecting a new take",
            next_command=command("compile_music_prompt_package.py", project_root),
            why="controlled regeneration and discard decisions mean the current take should not enter DAW, release, or catalog gates",
            do_not_do="do not use repair-route-map.md as permission to publish a take that was not locked",
        )
    if action == "repair candidate" and not route_suggests_daw(project_root):
        if not repair_regression_ready(project_root):
            return StageState(
                current_stage="repair-regression-gate",
                status="repair candidate",
                current_blocker="local repair route exists but 07_repair/regression-listen.md has not passed",
                next_action="perform the routed lyric/topline/structure/prompt repair, then fill regression-listen and update blind-review",
                next_command="",
                why="non-DAW repairs must prove the issue improved and no new AI artifacts were introduced before the project can move on",
                do_not_do="do not advance to release, catalog, or mastering from an unresolved local repair route",
            )
        return StageState(
            current_stage="take-selection-gate",
            status="repair verified",
            current_blocker="repair regression passed; best-take decision must be refreshed",
            next_action="rerun blind review and regenerate take-selection-decision from the repaired version",
            next_command=command("review_music_takes.py", project_root),
            why="a repaired take needs a fresh blind-review decision before it can become lock for post-production",
            do_not_do="do not treat regression-listen keep as release approval",
        )
    if route_suggests_daw(project_root) and not daw_handoff_ready(project_root) and not release_gate_ready(project_root):
        return StageState(
            current_stage="daw-humanization-gate",
            status="repair candidate",
            current_blocker="repair route points to stems/DAW/vocal/groove/mix work but no session pack is ready",
            next_action="prepare DAW/stems handoff and humanization session files",
            next_command=command("prepare_music_daw_handoff.py", project_root),
            why="a strong AI take should become editable material before it is treated as final audio",
            do_not_do="do not master or publish over unresolved stem, vocal, groove, or mix artifacts",
        )
    if not post_production_mix_lock_audit_ready(project_root) and not release_gate_ready(project_root):
        return StageState(
            current_stage="post-production-mix-lock-gate",
            status="release-candidate",
            current_blocker="post-production mix-lock audit is missing or not passed",
            next_action="fill post-review, arrangement-density-triage, mix-plan, mix-lock-preflight, and final-humanized-master-review, then audit post-production mix lock",
            next_command=command("audit_music_post_production_mix_lock.py", project_root),
            why="a locked take should not enter release listener or rights gates until arrangement density, vocal center, low-end pocket, space/dynamics, transitions/outro, and AI artifacts are locked",
            do_not_do="do not let loudness, rights approval, or repeated listening hide unresolved post-production AI flavor",
        )
    if not release_listener_audit_ready(project_root) and not release_gate_ready(project_root):
        return StageState(
            current_stage="release-listener-gate",
            status="release-candidate",
            current_blocker="external listener release evidence audit is missing or not passed",
            next_action="fill external-listener-panel, first-20-second-test, hook-memory-test, and ai-flavor-red-flag-survey, then audit listener evidence",
            next_command=command("audit_music_release_listener_evidence.py", project_root),
            why="public releases need outside-ear proof that the first 20 seconds hold, the hook is remembered, and AI-flavor red flags are low",
            do_not_do="do not let rights approval override weak listener pull, failed hook memory, or obvious AI flavor",
        )
    release_blocker = release_gate_blocker(project_root)
    if release_blocker or not audio_technical_ready(project_root) or not audio_qc_ready(project_root):
        blocker = release_blocker or "release gate is approved but required release audio evidence is incomplete"
        if not release_blocker and not audio_technical_ready(project_root):
            blocker = "release gate is missing and release-audio-technical-inspection.md has not passed"
            if release_gate_ready(project_root):
                blocker = "release gate is approved but release-audio-technical-inspection.md is not tool-generated with metrics"
        if not release_blocker and not audio_qc_ready(project_root):
            blocker = "release gate is missing and release-audio-qc-report.md has not passed"
            if release_gate_ready(project_root):
                blocker = "release gate is approved but release-audio-qc-report.md is not tool-generated with QC evidence"
        return StageState(
            current_stage="release-rights-gate",
            status="release-candidate",
            current_blocker=blocker,
            next_action="complete audio QC/listener/rights evidence, then prepare the release-candidate gate",
            next_command=command("prepare_music_release_candidate.py", project_root),
            why="audio quality, external feedback, rights, AI disclosure, and platform policy must be checked before publish-ready",
            do_not_do="do not mark publish-ready from musical preference alone",
        )
    if not catalog_memory_ready(project_root):
        return StageState(
            current_stage="catalog-memory-gate",
            status="publish-ready",
            current_blocker="release gate exists but catalog memory has not been written back",
            next_action="write catalog memory, prompt memory, artifact memory, and next-song seed",
            next_command=command("prepare_music_catalog_memory.py", project_root),
            why="a finished song should improve the next song instead of becoming an isolated one-off",
            do_not_do="do not use this song as a Persona, Voice, Custom Model, or catalog seed before promotion status is explicit",
        )
    return StageState(
        current_stage="catalog-memory-gate",
        status="catalog-updated",
        current_blocker="none",
        next_action="human review, archive evidence, then decide whether to release, revise, or start the next song",
        next_command="",
        why="all core automated gates have evidence and catalog writeback exists",
        do_not_do="do not keep editing without a new concrete review question",
    )


def evidence_table(project_root: Path) -> str:
    audio_seed_status = "not required"
    if audio_seed_required(project_root):
        audio_seed_status = "pass" if audio_seed_audit_ready(project_root) else "missing/incomplete"
    chord_skeleton_status = "not required"
    if chord_skeleton_transfer_required(project_root):
        chord_skeleton_status = "pass" if chord_skeleton_transfer_audit_ready(project_root) else "missing/incomplete"
    checks = [
        ("methodology transfer", METHODOLOGY_TRANSFER_AUDIT, methodology_transfer_audit_ready(project_root)),
        ("prompt specificity", PROMPT_SPECIFICITY_AUDIT, prompt_specificity_audit_ready(project_root)),
        ("genre/lane authenticity", GENRE_LANE_AUTHENTICITY_AUDIT, genre_lane_authenticity_audit_ready(project_root)),
        ("lyric narrative", LYRIC_NARRATIVE_AUDIT, lyric_narrative_audit_ready(project_root)),
        ("chord skeleton transfer", CHORD_SKELETON_TRANSFER_AUDIT, chord_skeleton_status),
        ("prompt package", "04_prompt/prompt-package-v001.md", prompt_package_ready(project_root)),
        ("prompt preflight", "04_prompt/prompt-preflight-review.md", prompt_preflight_ready(project_root)),
        ("prompt iteration discipline", PROMPT_ITERATION_DISCIPLINE_AUDIT, prompt_iteration_discipline_audit_ready(project_root)),
        ("generation evidence", "05_generations/generation-evidence-audit.md", generation_evidence_ready(project_root)),
        ("audio seed retention", AUDIO_SEED_AUDIT, audio_seed_status),
        ("take decision", "06_review/take-selection-decision.md", take_decision_ready(project_root)),
        ("repair route", "07_repair/repair-route-map.md", repair_route_ready(project_root)),
        ("repair regression", "07_repair/regression-listen.md", repair_regression_ready(project_root)),
        ("DAW handoff", "08_stems-daw/session-pack.md", daw_handoff_ready(project_root)),
        ("post-production mix lock", POST_PRODUCTION_MIX_LOCK_AUDIT, post_production_mix_lock_audit_ready(project_root)),
        ("audio technical inspection", "10_mix-master/release-audio-technical-inspection.md", audio_technical_ready(project_root)),
        ("audio QC", "10_mix-master/release-audio-qc-report.md", audio_qc_ready(project_root)),
        ("release listener evidence", RELEASE_LISTENER_AUDIT, release_listener_audit_ready(project_root)),
        ("release gate", "11_release/release-candidate-rights-gate.md", release_gate_ready(project_root)),
        ("release candidate pack", "11_release/release-candidate-evidence-audit.md + package + manifest", release_pack_ready(project_root)),
        ("catalog memory", "12_catalog-memory/catalog-writeback-manifest.md", catalog_memory_ready(project_root)),
    ]
    rows = ["| gate | evidence | status |", "|---|---|---|"]
    for gate, rel, ok in checks:
        status = ok if isinstance(ok, str) else ("pass" if ok else "missing/incomplete")
        rows.append(f"| {gate} | {rel} | {status} |")
    return "\n".join(rows)


